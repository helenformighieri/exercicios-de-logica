/*
Fatorial de um número: Escreva um programa que calcula o fatorial de um número dado pelo usuário.
*/

package chatGPT.diaUm;

public class Tres {
    public static void main(String[] args) {
        int fatorial = 5;

        for (int i = 1; i < 10; i++) {
                fatorial *= i;
            System.out.println(fatorial);
        }
    }
}